/**
 * op_add - Add 2 numbers
 *
 * @a: The first number
 * @b: The second number
 *
 * Return: The sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtract 2 numbers
 *
 * @a: The first number
 * @b: The second number
 *
 * Return: The difference between a - b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies 2 numbers
 *
 * @a: The first number
 * @b: The second number
 *
 * Return: The product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides 2 numbers
 *
 * @a: The first number
 * @b: The second number
 *
 * Return: The quotient of a and b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Finds the modulo of 2 numbers
 *
 * @a: The first number
 * @b: The second number
 *
 * Return: The mod of a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
